import (
	"os"
)


////////////////////////////////////////////////////////////////////////////////////////////////////

func parseFile(path string) ([]Function, error) {
	var allFuncs []Function
	content, err := os.ReadFile(path)
	if err != nil {
		return nil, err
	}
	funcs, err := parseShell(string(content))
	if err != nil {
		return nil, err
	}
	allFuncs = append(allFuncs, funcs...)
	return allFuncs, nil
}

