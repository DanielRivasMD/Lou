package cmd

////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"bytes"
	"fmt"

	"github.com/ttacon/chalk"
)


////////////////////////////////////////////////////////////////////////////////////////////////////

func cleanDir(location string) {
	// lineBreaks
	lineBreaks()

	// function call
	matchDir(location)

	// buffers
	var stdout bytes.Buffer
	var stderr bytes.Buffer

	// stdout
	fmt.Println(chalk.Cyan.Color(stdout.String()))

	// stderr
	if stderr.String() != "" {
		fmt.Println(chalk.Red.Color(stderr.String()))
	}

	// lineBreaks
	lineBreaks()
}

