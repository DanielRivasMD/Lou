package cmd

////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"os"
	"os/exec"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

func execGit(args ...string) {
	cmd := exec.Command("git", args...)

	// output terminal preserve color codes
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr

	// run the command
	err := cmd.Run()
	checkErr(err)
}

