package cmd

////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"os"
	"os/exec"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

func execCmd(cmd string, args ...string) {
	cmdRun := exec.Command(cmd, args...)

	// output terminal preserve color codes
	cmdRun.Stdout = os.Stdout
	cmdRun.Stderr = os.Stderr

	// run the command
	err := cmdRun.Run()
	checkErr(err)
}

