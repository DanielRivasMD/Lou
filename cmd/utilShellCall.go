package cmd

////////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"os"
	"os/exec"
)

////////////////////////////////////////////////////////////////////////////////////////////////////

func shellCall(cmd string) {
	cmdRun := exec.Command("bash", "-c", cmd)
	cmdRun.Stdout = os.Stdout
	cmdRun.Stderr = os.Stderr
	ε := cmdRun.Run()
	checkErr(ε)
}

